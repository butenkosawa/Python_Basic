# непарну ширину, він повинен бути вирівнен зсувом вправо.
# Використовуйте методи center() та rjust() для вирівнювання рядка.
#
# Приклад:
# Введіть рядок: Python
# Введіть ширину: 10
# Результат:
#   Python
# Введіть рядок: Python
