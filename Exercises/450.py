# У введеному списку цілих чисел, знайдіть і надрукуйте сусідні елементи, які мають однаковий знак.
# Якщо такої пари немає, не повинно нічого виводитися.
# Вхідні дані:
# 1 -2 -3 5 6 -3 7 8
# Вихідні дані:
# -2 -3
# 5 6
# 7 8

lst = [1, -2, -3, 5, 6, -3, 7, 8]

for i in range(len(lst) - 1):
    if lst[i] > 0 and lst[i+1] > 0:
        print(lst[i], lst[i+1])
    elif lst[i] < 0 and lst[i+1] < 0:
        print(lst[i], lst[i+1])
