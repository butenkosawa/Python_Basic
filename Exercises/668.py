# Напишіть функцію, яка створює комбінацію із
# двох послідовностей цілих чисел, впорядковану за зростанням.
# Вхідні дані:
# 1 4 0 12 4 5
# 24 1 2 10 8
# Вихідні дані:
# 0 1 1 2 4 4 5 8 10 12 24

def sort_lists(lst1, lst2):
    return sorted(lst1 + lst2)

lst1 = [1, 4, 0, 12, 4, 5,]
lst2 = [24, 1, 2, 10, 8]

for i in sort_lists(lst1, lst2):
    print(i, end= " ")