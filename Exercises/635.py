# Напишіть функцію, яка приймає послідовність чисел як у вхідних даних
# і створює список лише з першого та останнього елементів.
# Вхідні дані:
# 5 16 72 29 11 217 112
# Вихідні дані:
# [5, 112]

def first_last_number(*args):
    return [args[0], args[-1]]


print(first_last_number(5, 16, 72, 29, 11, 217, 112))