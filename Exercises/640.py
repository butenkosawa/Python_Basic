# Напишіть функцію для визначення суми трьох цілих чисел.
# Якщо будь-які два значення однакові, необхідно вивести нуль.
# Вхідні дані:
# 1 2 3
# 1 2 2
# Вихідні дані:
# 6
# 0

def sum_of_three_num(a, b, c):
    if any([a == b, a == c, b == c]):
        return 0
    return a + b + c


print(sum_of_three_num(1, 2, 3))
print(sum_of_three_num(1, 2, 2))