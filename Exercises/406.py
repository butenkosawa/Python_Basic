# Створіть список на основі введеної послідовності цілих чисел і надрукуйте його елементи таким чином:
# два останні елементи переміщені з кінця в початок списку без зміни їх початкового порядку.

input_sequence = input('Введіть послідовність цілих чисел: ')
lst = list(map(int, input_sequence.split()))
print(lst)
lst = lst[-2:] + lst[:-2]
print(lst)
