# Визначте кількість парних елементів в послідовності, яка завершується числом 0.
# Вводиться послідовність цілих чисел, що закінчується числом 0
# (саме число 0 в послідовність не входить, а використвоується як ознака її закінчення).

count_pass = 0

while True:
    num = int(input('Enter number: '))
    if num == 0:
        break
    elif num % 2 == 0:
        count_pass += 1

print(count_pass)
