# З клавіатури вводиться два цілих числа a і b. Напишіть програму, яка порівнює ці числа
# і в залежності від результату порівняння виводить на екран потрібний вираз,
# наприклад: 3 < 5 або 3 = 3 або 3 > 2. Команду print для виведення результату
# дозволяється використовувати лише один раз.

a = int(input("Enter number, a: "))
b = int(input("Enter number, b: "))
oper = ''

if a < b:
    oper = "<"
elif a > b:
    oper = ">"
else:
    oper = "="

print("a {} b".format(oper))