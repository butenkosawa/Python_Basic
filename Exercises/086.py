# Напишіть програму, яка зчитує два цілих числа a і b і виводить найбільше значення з них.
# Числа - цілі від 1 до 1000. При розв’язуванні задачі можна користуватися тільки
# цілочисельними арифметичними операціями. Не можна користуватися розгалуженнями, циклами, функціями.

num_1 = int(input('Enter number 1 (0...1000): '))
num_2 = int(input('Enter number 2 (0...1000): '))
great_num = int((num_1 + num_2 + abs(num_1 - num_2)) / 2)
print(great_num)
