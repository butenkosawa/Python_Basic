# Напишіть функцію для створення гістограми (наприклад, у вигляді *)
# із заданого списку цілих чисел як у вихідних даних.
# Формат введення списку чисел як у вхідних даних.
# Вхідні дані:
# 2,7,1,4,2,3,9,3
# Вихідні дані:
# **
# *******
# *
# ****
# **
# ***
# *********
# ***

def histogram(lst):
    for num in lst:
        print(num * '*')


histogram([2,7,1,4,2,3,9,3])