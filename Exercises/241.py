# Напишіть програму для побудови шаблону як у вихідних даних за введеним значенням n.
# Вхідні дані:
# 7
# Вихідні дані:
# 1  2 	3 	4 	5 	6 	7
# 2  3 	4 	5 	6 	7
# 3  4 	5 	6 	7
# 4  5 	6 	7
# 5  6 	7
# 6  7
# 7

n = 7

for i in range(0, n):
    for k in range(1, n+1):
        if k <= i:
            continue
        print(k, end="\t")
    print()