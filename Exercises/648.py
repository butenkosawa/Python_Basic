# Дано чотири дійсних числа: x1, y1, x2, y2. Напишіть функцію distance(x1, y1, x2, y2),
# яка обчислює відстань між точкою (x1, y1) і (x2, y2).
# Введіть чотири дійсних числа і виведіть результат роботи цієї функції.
# Вхідні дані:
# 0
# 0
# 1
# 1
# Вихідні дані:
# 1.41421

def distance(x1, y1, x2, y2):
    d = ((x2 - x1) ** 2 + (y2 - y1) ** 2) ** 0.5
    return f"Відстань між точками ({x1}, {y1}) та ({x2}, {y2}) становить: {d:.5f}"


print(distance(0,0,1,1))
