# Послідовність складається з натуральних чисел і завершується числом 0.
# Визначте значення найбільшого елемента послідовності. Вводиться послідовність цілих чисел,
# що закінчується числом 0 (саме число 0 в послідовність не входить, а використовується як ознака її закінчення).

max_el = 0

while True:
    num = int(input('Enter number: '))
    if num == 0:
        break
    elif num > max_el:
        max_el = num

print(max_el)
